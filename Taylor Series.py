import math
i = 3
n = 0
x = 0.25
sum1 = 0
sum2 = 0
sum3 = 0
# ----------------------------------------------------------------------------------------------------------------------
for n in range(n, i+1):
    sum1 += math.pow(-1, n) / math.factorial(2 * n + 1) * math.pow(x, 2 * n + 1)
    sum2 += math.pow(-1, n) / (2 * n + 1) * math.pow(x, 2 * n + 1)
    sum3 += math.pow(x, 2 * n + 1) / math.factorial(2 * n + 1)
# ----------------------------------------------------------------------------------------------------------------------
print('O resultado da somatória é: {:.10f}'.format(sum1 * sum2 * sum3))
